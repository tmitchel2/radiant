namespace Radiant.Scrolling;

/// <summary>Where a snapped element aligns within the viewport.</summary>
public enum SnapAlignment
{
    /// <summary>Align the snap point to the viewport's leading edge.</summary>
    Start,

    /// <summary>Centre the snap point in the viewport.</summary>
    Center,

    /// <summary>Align the snap point to the viewport's trailing edge.</summary>
    End,
}
